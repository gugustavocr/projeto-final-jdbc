    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try{
         ProfessorDTO professorDTO = new ProfessorDTO();
         
         professorDTO.setNome( txtNome.getText());
         professorDTO.setMateria( txtMateria.getText());
         professorDTO.setEmail( txtEmail.getText());
         professorDTO.setMatricula(Integer.parseInt(txtMatricula.getText()));
         
         new ProfessorDAO().cadastrar(professorDTO);
         
         JOptionPane.showMessageDialog(this,"Cadastro realizado com sucesso!");
          

         
       
       
         
       }
       catch(Exception e)
       {
          JOptionPane.showMessageDialog(this,"Erro ao Cadastra um Professor novo !");
       }
       
       
       txtNome.setText("");
       txtMateria.setText("");
       txtEmail.setText("");
       txtMatricula.setText("");
       
    }//GEN-LAST:event_jButton1ActionPerformed

